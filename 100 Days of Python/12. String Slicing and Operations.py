name = "Abhinav, Singh"
print(name[0:7])

# Finding Length of a string using len function
print(len(name))

# String slicing
print(name[0:7]) # Including 0 but not 7
print(name[1:7]) # Including 1 but not 7
print(name[:7]) # Automatically python put 0 to initial
print(name[0:-4]) # len() - the number which is given
print(name[0:len(name)-4]) # how python execute
print(name[-1:-3])

