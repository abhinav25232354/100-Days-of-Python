# Making calculator without conditional statements

print("Welcome to Abhinav's Calculator")
in1 = int(input("Enter First No.: "))
in2 = int(input("Enter Second No.: "))

print("Addition: ", in1+in2) # Addition
print("Subtraction: ", in1-in2) # Subtraction
print("Multiplication: ", in1*in2) # Multiplication
print("Division: ", in1/in2) # Division
print("Floor Division: ", in1//in2) # Floor Division (Return whole number without decimal)
print("Modular: ", in1%in2) # Modulas (Return remainder for the division)